#!/usr/bin/env node
/**
 * Downloads KJV JSON from https://github.com/aruljohn/Bible-kjv
 * into assets/bible/, writes bible-manifest.json, and lib/bibleBookLoaders.ts.
 */
import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const ASSET_DIR = join(ROOT, 'assets', 'bible');
const BASE =
  'https://raw.githubusercontent.com/aruljohn/Bible-kjv/master';

function bookToFilename(book) {
  return `${book.replace(/\s+/g, '')}.json`;
}

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.text();
}

async function main() {
  await mkdir(ASSET_DIR, { recursive: true });

  const booksUrl = `${BASE}/Books.json`;
  const booksRaw = await fetchText(booksUrl);
  await writeFile(join(ASSET_DIR, 'Books.json'), booksRaw, 'utf8');
  /** @type {string[]} */
  const bookNames = JSON.parse(booksRaw);

  const manifestBooks = [];
  let totalVerses = 0;

  for (const name of bookNames) {
    const file = bookToFilename(name);
    const url = `${BASE}/${file}`;
    const text = await fetchText(url);
    await writeFile(join(ASSET_DIR, file), text, 'utf8');
    const data = JSON.parse(text);
    const versesPerChapter = data.chapters.map((ch) => ch.verses.length);
    const verseCount = versesPerChapter.reduce((a, b) => a + b, 0);
    totalVerses += verseCount;
    manifestBooks.push({ name, verseCount, versesPerChapter });
    if (data.book !== name) {
      console.warn(`Book field mismatch: file ${file} has "${data.book}", expected "${name}"`);
    }
  }

  const manifest = { totalVerses, books: manifestBooks };
  await writeFile(
    join(ASSET_DIR, 'bible-manifest.json'),
    JSON.stringify(manifest),
    'utf8',
  );

  const loaderLines = bookNames
    .map(
      (name) => {
        const file = bookToFilename(name);
        return `  ${JSON.stringify(name)}: () => import("../assets/bible/${file}").then((m) => m.default as BibleBookJson),`;
      },
    )
    .join('\n');

  const loadersTs = `/* eslint-disable */
/* Generated by scripts/sync-bible-kjv.mjs — do not edit by hand */
import type { BibleBookJson } from "./bibleTypes";

export const bibleBookLoaders: Record<string, () => Promise<BibleBookJson>> = {
${loaderLines}
};
`;

  await writeFile(join(ROOT, 'lib', 'bibleBookLoaders.ts'), loadersTs, 'utf8');

  console.log(
    `Synced ${bookNames.length} books, ${totalVerses} verses → assets/bible + lib/bibleBookLoaders.ts`,
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
